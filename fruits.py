my_fruits = {"apple", "orange", "pineapple", "tomato"}
friend_fruits = {"banana", "cherry", "cashew", "fig"}

union_result = my_fruits | friend_fruits
print("Union: ", union_result)

intersection_result = my_fruits.intersection(friend_fruits)
print(intersection_result)

different_result = my_fruits - friend_fruits
print(different_result)
